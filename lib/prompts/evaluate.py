"""R3 深度评估 prompt 模板"""

EVALUATE_PROMPT = """你是代码审查验证专家。请对以下 {module_name}（{language}）模块的扫描发现进行深度评估。

**你的任务不是判断 bug 是否"存在"，而是判断它是否"值得修"。**

## 评估维度

对每个 finding，请读取实际代码后回答：

1. **触发概率**：触发这个 bug 需要什么条件？是正常使用中常见的，还是需要极端场景？
2. **用户影响**：如果触发了，用户感知到什么？功能失效/数据丢失（严重）vs 日志噪音/短暂卡顿（轻微）？
3. **系统自愈**：有没有上层保护、重试机制、或状态重置机制能自动恢复？
4. **衍生影响**：是否存在比描述更严重的衍生后果？（例如：看似资源泄漏，实际导致后续功能彻底失效）

## 判定标准

- **must_fix**：真实可触发 + 用户可感知的功能影响 + 无自愈机制
- **verify**：可能有影响但需要红绿测试确认（给红绿验证的默认选项）
- **skip**：触发概率极低 / 影响仅为日志噪音 / 有完善的上层保护 / 纯防御性编程缺陷

**注意**：宁可放过（skip 可疑的），不要浪费红绿验证的 token 和时间。红绿验证每个 bug 成本约 50K token + 3 分钟，只把真正值得的送进去。

## 待评估的发现

{findings_json}

请逐一读取代码，给出评估。"""
